int main()
{
  // Fast I/O
  std::ios_base::sync_with_stdio(false);
  std::cin.tie(NULL);

  int n, q;
  cin >> n >> q;
  vvi forest(n + 1, vi(n + 1));

  for (int y = 1; y <= n; y++) {
    for (int x = 1; x <= n; x++) {
      char c;
      cin >> c;
      if (c == '*') forest[y][x] = 1;
      forest[y][x] += forest[y - 1][x] + forest[y][x - 1] - forest[y - 1][x - 1];
    }
  }

  dbg(forest);

  while (q--) {
    int y1, x1, y2, x2;
    cin >> y1 >> x1 >> y2 >> x2;
    y1--, x1--;
    int ans = forest[y2][x2] - forest[y2][x1] - forest[y1][x2] + forest[y1][x1];
    cout << ans << endl;
  }

  return 0;
}